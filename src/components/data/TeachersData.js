// Helper to interpolate variables inside template strings
const interpolate = (template, variables) => {
  let result = template;
  Object.keys(variables).forEach(key => {
    result = result.replace(new RegExp(`{${key}}`, 'g'), variables[key]);
  });
  return result;
};

